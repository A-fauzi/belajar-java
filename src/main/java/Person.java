// Abstract
// Sebuah abstract metode milik abstract kelas, dan tidak memiliki tubuh. Tubuh disediakan oleh subclass:

// abstract class
public class Person {
    private String name; // private = restricted access

    // Getter
    public String getName() {
        return name;
    }

    // Setter
    public void setName(String name) {
        this.name = name;
    }

}
